def product(a, b):
    """Return product of a and b.
        >>> product(2, 2)
        4
        >>> product(2, -2)
        -4
    """
    return a * b
    
def addition(a, b):
    """Return product of a and b.
        >>> product(2, 2)
        4
        >>> product(2, -2)
        -4
    """
    return a + b
    
def subtraction(a, b):
    """Return product of a and b.
        >>> product(2, 2)
        4
        >>> product(2, -2)
        -4
    """
    return a - b

def division(a, b):
    """Return product of a and b.
        >>> product(2, 2)
        4
        >>> product(2, -2)
        -4
    """
    return a / b
    
     
